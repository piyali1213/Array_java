package july8switchloop;

public class WhileLoopTable2 {
    public static void main(String[] args) {
        int num1;
        int num2;

        num1 = 2;
        while (num1 <= 10) {
            num2 = num1 * 2;
            num1++;
            System.out.println("result : " + num2);

        }
    }
}


